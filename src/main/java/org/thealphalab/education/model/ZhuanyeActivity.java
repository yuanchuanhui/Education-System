  private String aname;
  private int ajibie;
  private String asort;
  private boolean adelsign;

  public int getAid() {
    return aid;
  }

  public void setAid(int aid) {
    this.aid = aid;
  }

  public String getAname() {
    return aname;
  }

  public void setAname(String aname) {
    this.aname = aname;
  }

  public int getAjibie() {
    return ajibie;
  }

  public void setAjibie(int ajibie) {
    this.ajibie = ajibie;
  }

